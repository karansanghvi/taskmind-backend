const mongoose = require('mongoose');

// Define the signup schema
const SignupSchema = new mongoose.Schema({
  fullName: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
    lowercase: true,
  },
  password: {
    type: String,
    required: true,
    minlength: 6,
  },
}, {
  timestamps: true,
});

// Export the model
const SignupModel = mongoose.model('Signup', SignupSchema);
module.exports = SignupModel;
